    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        
        
        DefaultTableModel table = (DefaultTableModel) dataTable.getModel();
        Vector a = new Vector();
        if(palletCode.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "null");
        }else if(lotCode.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "null");
        }else if(lineCode.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "null");
        }
        else{
            int n=1;
            for(int e=0;e<table.getRowCount();e++){
                n +=1;
            }
            String tgl = new SimpleDateFormat("yyyy-MM-dd").format(new Date());
            
            a.add(n);
            a.add(palletCode.getText());
            a.add(lotCode.getText());
            a.add(lineCode.getText());
            a.add(clock.getText());
            a.add(remark.getItemAt(remark.getSelectedIndex()));
            
            a.add(tgl);
            table.addRow(a);

            palletCode.setText("");
            lotCode.setText("");
            lineCode.setText("");
   
        
        }
        
       
        
        
        
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

