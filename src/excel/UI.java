    private void exportExcelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_exportExcelActionPerformed
        // TODO add your handling code here:   
   if(fileDir.getText().trim().isEmpty()){
       JOptionPane.showMessageDialog(palletCode, "You must choose directory!");
   }else if(fileDir.getText().trim().isEmpty()){
       JOptionPane.showMessageDialog(palletCode, "null Name!");
   }else{
       try{
        DefaultTableModel model =(DefaultTableModel) dataTable.getModel();
        
        String file = fileName.getText();
        String directory = fileDir.getText();
        directory = directory.replace('\\','/');
        FileWriter excel = new FileWriter(directory+'/'+file+".xls");
        for(int i = 0; i < model.getColumnCount(); i++){
            excel.write(model.getColumnName(i) + "\t");
        }

        excel.write("\n");

        for(int i=0; i< model.getRowCount(); i++) {
            for(int j=0; j < model.getColumnCount(); j++) {
                excel.write(model.getValueAt(i,j).toString()+"\t");
            }
            excel.write("\n");
        }

        excel.close();

    }catch(IOException e){ System.out.println(e); }
        JOptionPane.showMessageDialog(palletCode, "Saved !");
        fileDir.setText("");
        fileName.setText("");
   }
        
       
    }//GEN-LAST:event_exportExcelActionPerformed

